    Route::get('/{id}/edit', [PostController::class, 'edit'])->name('edit');
    Route::post('/{id}/update', [PostController::class, 'update'])->name('update');
    Route::post('/{id}/delete', [PostController::class, 'delete'])->name('delete');
});
